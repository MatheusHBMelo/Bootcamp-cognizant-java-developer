public class Retorno{
    // Função retornando inteiro
    public static int somarNumeros(int a, int b){
        return a + b;
    }

    // Função retornando String
    public static String retornarNome(String nome){
        return nome;
    }

    // Função retornando Float
    public static float descontaSalario(float salario, float desconto){
        return salario - desconto;
    }

    // Função retornando Double
    public static double somarDinheiro(double d1, double d2){
        return d1 + d2;
    }

    // Função retornando Long
    public static long juntaCpf(long cpf){
        return cpf;
    }

    // Função retornando Char
    public static char letraNome(char x){
        return x;
    }

    // Função retornando void
    public static void nomeCompleto(){
        System.out.println("Matheus Henrique Barbosa de Melo");
    }
}